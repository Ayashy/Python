
# print is statement which returns what lies inside the brackets
# print returns "hello world" in the statement below

print("hello world")

# "hello world" is in quotations marks.
# Something inside quotation marks is called a String.


greeting = "hello world"
# this String is now stored inside the word greeting.
# something that stores something inside of it like this is called a Variable

print(greeting)
# Like Strings, we can print Variables too!
